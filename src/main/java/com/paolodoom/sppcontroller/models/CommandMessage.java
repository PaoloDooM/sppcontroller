package com.paolodoom.sppcontroller.models;

/**
 *
 * @author PaoloDooM
 */
public class CommandMessage {

    private String uuid;
    private String type;
    private String data;
    private int code;
    private String message;

    public CommandMessage(String uuid, String type, String data, int code, String message) {
        this.uuid = uuid;
        this.type = type;
        this.data = data;
        this.code = code;
        this.message = message;
    }

    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
}
